#valor, mas imprima na tela informando se ele é POSITIVO ou NEGATIVO.



num = int(input("Digite um Número: ")) 

if num >= 0: 
    print(f"\nNúmero {num}, é Positivo\n") 
else: 
    print(f"Número {num}, é Negativo\n")